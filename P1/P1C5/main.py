"""
Exercice P1C5 – Types de données en Python

Objectifs :
- Manipuler différents types de variables
- Afficher leurs valeurs
- Identifier leur type avec type()
"""

# Déclaration des variables
nom = "Essuthi Mbangué"
age = 25
taille = 1.71
est_etudiant = True

# Affichage des valeurs
print(f"Nom: {nom}")
print(f"Age: {age}")
print(f"Taille: {taille}")
print(f"Est étudiant: {est_etudiant}")

# Affichage des types
print(f"Type nom: {type(nom)}")
print(f"Type age: {type(age)}")
print(f"Type taille: {type(taille)}")
print(f"Type est étudiant: {type(est_etudiant)}")

